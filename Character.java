class Character{
    String name;
    int hp;
    int mp;
    int atk;
    int def;
    int lucky;
    public Character(String name, int hp, int mp, int atk, int def, int lucky){
        this.name = name;
        this.hp = hp;
        this.mp = mp;
        this.atk = atk;
        this.def = def;
        this.lucky = lucky;
    }
}